export function getSectorMedians(vertical: Vertical): {
  peNTM: number
  evEbitdaNTM: number
  evRevNTM: number
  ebitdaMargin: number
  revGrowth: number
} {
  const group = companies.filter((c) => c.vertical === vertical)
  const median = (arr: number[]) => {
    const s = [...arr].sort((a, b) => a - b)
    const m = Math.floor(s.length / 2)
    return s.length % 2 !== 0 ? s[m] : (s[m - 1] + s[m]) / 2
  }
  return {
    peNTM: median(group.filter((c) => c.peNTM !== null).map((c) => c.peNTM as number)),
    evEbitdaNTM: median(group.map((c) => c.evEbitdaNTM ?? 0)),
    evRevNTM: median(group.map((c) => c.evRevNTM ?? 0)),
    ebitdaMargin: median(group.map((c) => c.ebitdaMargin)),
    revGrowth: median(group.map((c) => c.revGrowth)),
  }
}
